package com.java.bean;

public class Product {
	
		private int cas_no;
		private String chem_name;
		private int gst;
		private int pack_size;
		private int rs_per_pack;
		public int getCas_no() {
			return cas_no;
		}
		public void setCas_no(int cas_no) {
			this.cas_no = cas_no;
		}
		public String getChem_name() {
			return chem_name;
		}
		public void setChem_name(String chem_name) {
			this.chem_name = chem_name;
		}
		public int getGst() {
			return gst;
		}
		public void setGst(int gst) {
			this.gst = gst;
		}
		public int getPack_size() {
			return pack_size;
		}
		public void setPack_size(int pack_size) {
			this.pack_size = pack_size;
		}
		public int getRs_per_pack() {
			return rs_per_pack;
		}
		public void setRs_per_pack(int rs_per_pack) {
			this.rs_per_pack=rs_per_pack;
		}
		


}

